<h3 id="enquete-de-flagrance" tabindex="-1"><a class="header-anchor" href="#enquete-de-flagrance" aria-hidden="true">#</a> Enquête de flagrance</h3>
<p><em>(</em><a href="https://www.legifrance.gouv.fr/affichCode.do%3Bjsessionid%3DE6E501FF17912F1D6E10031EB1C0B625.tpdila18v_3?idSectionTA=LEGISCTA000006151876&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20170529" target="_blank" rel="noopener noreferrer"><em>art 53 à
73</em><ExternalLinkIcon/></a><em>)</em></p>
<pre><code>Conditions :
</code></pre>
<ul>
<li>
<p>un des 4 critères de l'<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000006575016&amp;cidTexte=LEGITEXT000006071154" target="_blank" rel="noopener noreferrer">art
53<ExternalLinkIcon/></a>
:</p>
<ul>
<li>
<p>infraction en cours (attention aux infractions continues),</p>
</li>
<li>
<p>infraction qui vient de se commettre,</p>
</li>
<li>
<p>auteur poursuivi par la clameur publique (plainte victime, dénonciation,
<em>etc.</em>),</p>
</li>
<li>
<p>auteur trouvé en possession de traces ou indices (à la suite d'un
contrôle visuel / palpation de sécurité mais pas à la suite d'une
fouille, qui suppose qu'une enquête soit déjà ouverte) ;</p>
</li>
</ul>
</li>
<li>
<p>condition temporelle : 24h pour le 2e critère et 48h pour les deux derniers
(sur ce point, voir
<a href="http://questions.assemblee-nationale.fr/q14/14-70710QE.htm" target="_blank" rel="noopener noreferrer">question<ExternalLinkIcon/></a>
<a href="http://questions.assemblee-nationale.fr/q14/14-70710QE.htm" target="_blank" rel="noopener noreferrer">écrite<ExternalLinkIcon/></a> au
ministère de la justice) ;</p>
</li>
<li>
<p>un ou plusieurs indices apparents d'un comportement délictueux (<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000007062231&amp;fastReqId=1562939541&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 4
janvier
1982<ExternalLinkIcon/></a>)
ce que ne sont pas le renseignement ou la dénonciation anonyme non étayés
(<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?idTexte=JURITEXT000007060306" target="_blank" rel="noopener noreferrer">Crim., 30 mai
1980<ExternalLinkIcon/></a>
et
<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?idTexte=JURITEXT000007063769" target="_blank" rel="noopener noreferrer">Crim.,<ExternalLinkIcon/></a>
<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?idTexte=JURITEXT000007063769" target="_blank" rel="noopener noreferrer">2 février
1988<ExternalLinkIcon/></a>)
mais il en va différemment si le témoin s'identifie (<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000007069119&amp;fastReqId=780038330&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 1er octobre
2003<ExternalLinkIcon/></a>)
ou que le renseignement anonyme est étayé par d'autres indices ;</p>
</li>
<li>
<p>une infraction punie d'une peine d'emprisonnement (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000020632128&amp;cidTexte=LEGITEXT000006071154" target="_blank" rel="noopener noreferrer">art
67<ExternalLinkIcon/></a>
; <em>cf</em>
<a href="http://intranet.justice.gouv.fr/site/dacg/art_pix/icpnpe_doc_DACG.pdf" target="_blank" rel="noopener noreferrer">liste<ExternalLinkIcon/></a>
des délits non punissables de l'emprisonnement). La Cass contrôle
l'incrimination initialement retenue par les enquêteurs et peu importe que
&quot;<em>par la suite, les faits aient reçu une qualification contraventionnelle</em>&quot;
(<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000007065095&amp;fastReqId=11976448&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 9 janvier
1990<ExternalLinkIcon/></a>).</p>
</li>
</ul>
<p>Durée de 8 jours à compter du premier acte d'investigation (et non de
l'infraction : <a href="http://www.justice.gouv.fr/bulletin-officiel/dacg76e.htm#2211" target="_blank" rel="noopener noreferrer">circ.
du<ExternalLinkIcon/></a> <a href="http://www.justice.gouv.fr/bulletin-officiel/dacg76e.htm#2211" target="_blank" rel="noopener noreferrer">31 décembre
1999<ExternalLinkIcon/></a>, § 2.2.1.1)
renouvelable une fois par PR pour infraction punie d'une peine d'emprisonnement
≥ à 5 ans (<a href="https://www.legifrance.gouv.fr/codes/article_lc/LEGIARTI000006575016/" target="_blank" rel="noopener noreferrer">art
53<ExternalLinkIcon/></a>, al
3). On n'a pas besoin d'attendre le 8e jour pour décider de la prolongation de
la flagrance. Même si ça n'est pas nécessaire, il vaut mieux passer par un écrit
pour prolonger la flagrance (§ 2.2.2 de la <a href="http://intranet.justice.gouv.fr/dacg/cabinet//docs/2004/circ_140504.pdf" target="_blank" rel="noopener noreferrer">circ. du 14 mai
2004<ExternalLinkIcon/></a>).</p>
<p>Impératif de continuité : pour tenir la flagrance, il faut au moins un acte par
jour en ce inclus les jours fériés et week-ends (le PV relatant l'acte n'a
toutefois pas à être impérativement rédigé le jour même :
<a href="https://www.legifrance.gouv.fr/juri/id/JURITEXT000007067843?page=1&amp;pageSize=10&amp;query=91-86502&amp;searchField=ALL&amp;searchType=ALL&amp;sortValue=DATE_DESC&amp;tab_selection=juri&amp;typePagination=DEFAULT" target="_blank" rel="noopener noreferrer">Crim.,<ExternalLinkIcon/></a>
<a href="https://www.legifrance.gouv.fr/juri/id/JURITEXT000007067843?page=1&amp;pageSize=10&amp;query=91-86502&amp;searchField=ALL&amp;searchType=ALL&amp;sortValue=DATE_DESC&amp;tab_selection=juri&amp;typePagination=DEFAULT" target="_blank" rel="noopener noreferrer">11 février
1992<ExternalLinkIcon/></a>).
Lorsque la flagrance est expirée, elle ne peut plus être réactivée.</p>
<p>C'est l'appréciation des enquêteurs qui compte pour qualifier la flagrance et
les juges ne peuvent apprécier les conditions de la flagrance <em>a posteriori</em>
(<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000007067224&amp;fastReqId=1294360833&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 22 février
1996<ExternalLinkIcon/></a>
; <a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000041481930&amp;fastReqId=577521082&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 7 janvier
2020<ExternalLinkIcon/></a>).
Ce point n'est pas très clair car la Cass a déjà admis que les juges pouvaient
relever la flagrance là où les enquêteurs ont agi en prélim (<a href="https://www.legifrance.gouv.fr/juri/id/JURITEXT000024647435/" target="_blank" rel="noopener noreferrer">Crim., 5 octobre
2011<ExternalLinkIcon/></a> et <a href="https://www.legifrance.gouv.fr/juri/id/JURITEXT000029152469/" target="_blank" rel="noopener noreferrer">Crim.,
26 juin 2014<ExternalLinkIcon/></a>).</p>
<h3 id="enquete-preliminaire" tabindex="-1"><a class="header-anchor" href="#enquete-preliminaire" aria-hidden="true">#</a> Enquête préliminaire</h3>
<p><em>(</em><a href="https://www.legifrance.gouv.fr/affichCode.do?idSectionTA=LEGISCTA000006151877&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20170529" target="_blank" rel="noopener noreferrer"><em>art 75 à
78</em><ExternalLinkIcon/></a><em>)</em></p>
<pre><code>Spécificités procédurales :
</code></pre>
<ul>
<li>
<p>réquisition nécessite l'accord du PR (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do%3Bjsessionid%3D555CDA05B33E70E0B4AF82765E1131E3.tplgfr33s_1?idArticle=LEGIARTI000038311815&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20190325" target="_blank" rel="noopener noreferrer">art 77-1 et
s.<ExternalLinkIcon/></a>)
;</p>
</li>
<li>
<p>prélèvement externe sur suspect ou témoin nécessite l'accord du PR (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000038311823&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20190325" target="_blank" rel="noopener noreferrer">art
76-2<ExternalLinkIcon/></a>
et
<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?cidTexte=LEGITEXT000006071154&amp;idArticle=LEGIARTI000006575022&amp;dateTexte&amp;categorieLien=cid" target="_blank" rel="noopener noreferrer">55-1<ExternalLinkIcon/></a>)
;</p>
</li>
<li>
<p>perquisition, fouille et saisie nécessitent l'assentiment du MEC (sauf
accord du JLD pour infraction punie de 3 ans ou plus : <a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?cidTexte=LEGITEXT000006071154&amp;idArticle=LEGIARTI000006575124&amp;dateTexte&amp;categorieLien=cid" target="_blank" rel="noopener noreferrer">art
76<ExternalLinkIcon/></a>)
;</p>
</li>
<li>
<p>l'interpellation nécessite l'accord du PR (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do%3Bjsessionid%3D86F5F4ADD3F55FC0AB128E97500D9DD7.tpdila17v_3?idArticle=LEGIARTI000032655614&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20170605&amp;categorieLien=id&amp;oldAction&amp;nbResultRech" target="_blank" rel="noopener noreferrer">art
78<ExternalLinkIcon/></a>
ou mandat de recherche). Pour une interpellation dans un domicile, il faut
<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?cidTexte=LEGITEXT000006071154&amp;idArticle=LEGIARTI000006575124&amp;dateTexte&amp;categorieLien=cid" target="_blank" rel="noopener noreferrer">art
76<ExternalLinkIcon/></a>
ou mandat de recherche et c'est tjrs entre 6 et 21 heures.</p>
</li>
</ul>
<p>Lorsqu'une enquête menée en flag aurait dû l'être en prélim, les actes accomplis
sont valides dès lors qu'ils auraient pu être réalisés en prélim (<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000007609407&amp;fastReqId=934111015&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 14
septembre
2004<ExternalLinkIcon/></a>).</p>
<p>Dans l'hypothèse où un renseignement anonyme n'est étayé par aucun autre élément
matériel suffisant, seule une enquête préliminaire peut être ouverte : ledit
renseignement anonyme ne pouvant être retenu comme moyen unique de preuve
(<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000023165068&amp;fastReqId=185619463&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 9 novembre
2010<ExternalLinkIcon/></a>).
Au cours de l'enquête prélim, devront être découverts des indices pour basculer
en flag (<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000024647435&amp;fastReqId=132990635&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 5 octobre
2011<ExternalLinkIcon/></a>).</p>
<p>L'<a href="https://www.legifrance.gouv.fr/codes/article_lc/LEGIARTI000044556711" target="_blank" rel="noopener noreferrer">art
75-3<ExternalLinkIcon/></a>
prévoit que, pour celles débutées à compter du 23 décembre 2021, les enquêtes
prélim sont d'une durée maximale de :</p>
<ul>
<li>2 ans à compter du premier acte d'enquête lorsqu'elles portent sur des
infractions de droit commun ;</li>
</ul>
<p>durée prolongeable 1 fois pour 1 an par décision écrite du PR ;</p>
<ul>
<li>3 ans à compter du premier acte d'enquête lorsqu'elles portent sur des
infractions visées par les art 706-73 et 706-73-1 du CPP ; durée
prolongeable 1 fois pour 2 ans par décision écrite du PR.</li>
</ul>
<p>Suspension des délais si CSS ou coopération. Nullité des actes au-delà sauf
s'ils concernent une personne mise en cause depuis une durée inférieure à ces
délais.</p>
<p><em>Ouverture au contradictoire</em> : à tout moment, et même en l'absence de demande
en ce sens, le parquet peut communiquer tout ou partie de la procédure au MEC et
à la victime pour recueillir leurs observations ( <a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000032655262&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20160605" target="_blank" rel="noopener noreferrer">art
77-<ExternalLinkIcon/></a>
<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000032655262&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20160605" target="_blank" rel="noopener noreferrer">2<ExternalLinkIcon/></a>,
I). Il est tenu de le faire à l'égard du MEC lorsqu'un délai de 2 ans s'est
écoulé depuis son audition ou la perquisition réalisée à son domicile (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000032655262&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20160605" target="_blank" rel="noopener noreferrer">art
77-2<ExternalLinkIcon/></a>,
V).</p>
<p>De même, le MEC, soupçonné d'avoir commis une infraction punie d'emprisonnement,
peut accéder à la procédure dans 3 cas : un an après son audition, un an après
une perquisition réalisée chez lui et en cas d'atteinte à la présomption
d'innocence (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000032655262&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20160605" target="_blank" rel="noopener noreferrer">art 77-2<ExternalLinkIcon/></a>,
II). Le PR peut refuser pour une durée maximale de 6 mois (décision pouvant être
contestée devant le PG) et peut ne pas communiquer certaines pièces en cas de
risque de pression.</p>
<h3 id="changement-de-cadre-d-enquete" tabindex="-1"><a class="header-anchor" href="#changement-de-cadre-d-enquete" aria-hidden="true">#</a> Changement de cadre d'enquête</h3>
<p>(<em>cf</em> <a href="http://intranet.cour-de-cassation.intranet.justice.fr/rpvjcc/Reserve/SDE/BDD/CR/20180630_cr_changement_cadre_enquete.pdf" target="_blank" rel="noopener noreferrer">note SDER - mars2018<ExternalLinkIcon/></a>)</p>
<p>Si une procédure peut porter sur des faits distincts, il ne doit y avoir qu'un
seul cadre d'enquête par procédure.</p>
<p>Par ailleurs, dans une même procédure, on ne peut enquêter en flag puis en
prélim puis revenir en flag (<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000028355879&amp;fastReqId=1368435522&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim., 18 décembre
2013<ExternalLinkIcon/></a>)
: l'enquête de flag ne peut pas être utilisée à plusieurs reprises dans une même
procédure à des moments distincts.</p>
<p>Le passage de la préliminaire à la flagrance est possible (sous réserve qu'il
n'y ait pas déjà eu de flag par le passé) :</p>
<ul>
<li>
<p>faits identiques : si la procédure porte sur les mêmes faits (les critères
de la flagrance sont absents au début d'une enquête mais apparaissent par la
suite) : la mutation de l'enquête préliminaire en flag est toujours possible
lorsqu'il n'y a pas eu de flagrance initialement et que les conditions de la
flagrance sont réunies (ex : <a href="https://www.legifrance.gouv.fr/juri/id/JURITEXT000020744297?page=1&amp;pageSize=10&amp;query=09-81.434&amp;searchField=ALL&amp;searchType=ALL&amp;sortValue=DATE_DESC&amp;tab_selection=juri&amp;typePagination=DEFAULT" target="_blank" rel="noopener noreferrer">Crim., 12 mai 2009<ExternalLinkIcon/></a>).
Des indices apparents d'un comportement délictueux peuvent effectivement
être découverts lors d'opérations de police réalisées en prélim ou dans un
autre cadre (<a href="https://www.legifrance.gouv.fr/juri/id/JURITEXT000007069047?page=1&amp;pageSize=10&amp;query=99-85.397&amp;searchField=ALL&amp;searchType=ALL&amp;sortValue=DATE_DESC&amp;tab_selection=juri&amp;typePagination=DEFAULT" target="_blank" rel="noopener noreferrer">Crim., 4 novembre
1999<ExternalLinkIcon/></a>).</p>
</li>
<li>
<p>faits distincts :</p>
<ul>
<li>
<p>soit on a une connexité entre les faits flagrants et les faits non
flagrants. Quand on a des infractions qui relèveraient de la prélim et
d'autres qui relèveraient du flag et que l'on décide de réaliser une
procédure unique, on ne basculera en flag pour l'ensemble que s'il existe
entre les faits objets des investigations des rapports de connexité (<a href="https://www.legifrance.gouv.fr/affichJuriJudi.do?oldAction=rechJuriJudi&amp;idTexte=JURITEXT000035974653&amp;fastReqId=1648588329&amp;fastPos=1" target="_blank" rel="noopener noreferrer">Crim.,31 octobre 2017<ExternalLinkIcon/></a>)
; <em>a contrario</em>, des faits de même nature impliquant le même individu ne
suffisent pas à établir la connexité ;</p>
</li>
<li>
<p>soit il s'agit d'infractions vraiment distinctes et on gardera les deux
cadres d'enquête distincts et donc deux procédures.</p>
</li>
</ul>
</li>
</ul>
<h2 id="rcm-rcb-rcd" tabindex="-1"><a class="header-anchor" href="#rcm-rcb-rcd" aria-hidden="true">#</a> RCM/RCB/RCD</h2>
<p><strong>Enquête aux fins de recherche des causes de la mort : RCM</strong> (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?cidTexte=LEGITEXT000006071154&amp;idArticle=LEGIARTI000006575114" target="_blank" rel="noopener noreferrer">art 74 CPP<ExternalLinkIcon/></a>),
dite <em>enquête décès</em></p>
<p><strong>Enquête aux fins de recherche des causes des blessures : RCB</strong> (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?cidTexte=LEGITEXT000006071154&amp;idArticle=LEGIARTI000006575114" target="_blank" rel="noopener noreferrer">art 74
CPP<ExternalLinkIcon/></a>)</p>
<p><strong>Enquête aux fins de recherche des causes de la disparition : RCD</strong> (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000006575117&amp;cidTexte=LEGITEXT000006071154" target="_blank" rel="noopener noreferrer">art
74-1
CPP<ExternalLinkIcon/></a>)</p>
<p>Pour que les enquêteurs ouvrent une telle procédure, il faut l'<strong>accord initial
du PR</strong> (il faut le faire acter) car les pouvoirs sont proches de la flagrance
(renvoi aux art 56 à 62) bien que les actes coercitifs (GAV ou interpellation)
soient toutefois exclus (la rétention de témoin semble autorisée : § 1.2 de la
<a href="http://intranet.justice.gouv.fr/site/dacg/art_pix/circ_190710_loi_simplif_du_120509_close.pdf" target="_blank" rel="noopener noreferrer">circ. du 19
juillet 2010<ExternalLinkIcon/></a>).
La géolocalisation est possible pour la RCM et RCD (<a href="https://www.legifrance.gouv.fr/codes/article_lc/LEGIARTI000038311484/" target="_blank" rel="noopener noreferrer">art
230-32<ExternalLinkIcon/></a>,
2°).</p>
<p>Ces enquêtes durent 8 jours non renouvelables (à compter de l'ouverture de
l'enquête et non de la mort ou de la disparition) et au terme :</p>
<ul>
<li>
<p>soit on ouvre une info en RCM/RCD (pas d'info en RCB) ce qui permet de
mettre en place des écoutes téléphoniques ;</p>
</li>
<li>
<p>soit on part en enquête en la forme préliminaire ;</p>
</li>
<li>
<p>soit on classe 11 ;</p>
</li>
<li>
<p>soit l'enquête révèle l'existence apparente d'une infraction et on bascule
en enquête préliminaire / de flagrance ou on ouvre une information.</p>
<p>Dès que l'on bascule d'un cadre d'enquête à un autre, on fait acter par
l'OPJ.</p>
</li>
</ul>
<p>Le parquet compétent est celui du lieu de découverte du cadavre / de la personne
grièvement blessée (RCM/B) ou de la disparition (RCD).</p>
<ul>
<li><strong>RCM</strong> / <strong>RCB</strong> (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?cidTexte=LEGITEXT000006071154&amp;idArticle=LEGIARTI000006575114" target="_blank" rel="noopener noreferrer">art 74
CPP<ExternalLinkIcon/></a>
; <a href="http://intranet.justice.gouv.fr/site/dacg/art_pix/Memento_de_perm_Morts_et_blessures_graves%202019.pdf" target="_blank" rel="noopener noreferrer">fiche DACG - octobre
2019<ExternalLinkIcon/></a>)</li>
</ul>
<p>Cette enquête suppose une mort (art 74, al 1) ou des blessures graves (art 74,
al 6) dont la cause est inconnue ou suspecte.</p>
<p>L'OPJ avise le parquet et se transporte sur place pour procéder aux 1res
constatations.</p>
<p>Avec un cadavre suspect découvert dans un appart où il y a bcp de stup : enquête
en RCM + enquête distincte en flagrance sur ILS pour identifier complices /
coauteurs.</p>
<p>Aucune procédure judiciaire en l'absence d'OML (simple procédure civile).</p>
