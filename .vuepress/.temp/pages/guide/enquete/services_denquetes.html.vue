<h2 id="competence-territoriale" tabindex="-1"><a class="header-anchor" href="#competence-territoriale" aria-hidden="true">#</a> Compétence territoriale</h2>
<p>Désormais, les OPJ peuvent intervenir sur tout le territoire national. Sauf s'il
s'agit d'un département limitrophe à leur ressort de compétence, ils doivent
informer de leur déplacement le PR saisi de l'enquête et le PR du ressort dans
lequel ils interviennent (<a href="https://www.legifrance.gouv.fr/codes/article_lc/LEGIARTI000042779661/2020-12-27" target="_blank" rel="noopener noreferrer">art
18<ExternalLinkIcon/></a>
et <a href="https://www.legifrance.gouv.fr/codes/article_lc/LEGIARTI000043999502/2021-09-01/" target="_blank" rel="noopener noreferrer">D.
12<ExternalLinkIcon/></a>).
Le PR saisi de l'enquête peut leur adjoindre un OPJ territorialement compétent.
Il n'est donc plus nécessaire d'autoriser une extension de compétence
territoriale (<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?cidTexte=LEGITEXT000006071154&amp;idArticle=LEGIARTI000032655589" target="_blank" rel="noopener noreferrer">ancien art
18<ExternalLinkIcon/></a>,
al 4).</p>
<p>Si le PR dirige l'activité des OPJ et APJ de son ressort, l'<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do%3Bjsessionid%3DD6E55E6052B0A6A81FE1A70CD834775F.tplgfr25s_3?idArticle=LEGIARTI000038311420&amp;cidTexte=LEGITEXT000006071154&amp;categorieLien=id&amp;dateTexte=20200324" target="_blank" rel="noopener noreferrer">art
41<ExternalLinkIcon/></a>,
al 2 (<em>cf</em>
<a href="http://faq.dacg.intranet.justice.gouv.fr/index.php?act=2&amp;&amp;page=63&amp;selection=1919&amp;1919" target="_blank" rel="noopener noreferrer">Q1919<ExternalLinkIcon/></a>
FAQ DACG), lui permet de requérir tout OPJ sur l'<strong>ensemble du territoire
national</strong> de procéder aux actes d'enquête qu'il estime nécessaires dans les
lieux où chacun d'eux est territorialement compétent (on n'a donc plus besoin de
passer par la rédaction de l'<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do%3Bjsessionid%3D19ECF3D7BE15C5B229271BCC4DCF8059.tpdila13v_3?idArticle=LEGIARTI000006514839&amp;cidTexte=LEGITEXT000006071154&amp;categorieLien=id&amp;dateTexte=20161030" target="_blank" rel="noopener noreferrer">ancien art D.
