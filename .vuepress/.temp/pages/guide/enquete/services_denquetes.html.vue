<h2 id="service-d-enquetes-judiciaires-des-finances-sejf" tabindex="-1"><a class="header-anchor" href="#service-d-enquetes-judiciaires-des-finances-sejf" aria-hidden="true">#</a> Service d'enquêtes judiciaires des finances (SEJF)</h2>
<p><em>(</em><a href="http://intranet.justice.gouv.fr/site/dacg/art_pix/Depeche_2019-07-17_Creation_service_enquetes_judiciaires_des_finances_SEJF.pdf" target="_blank" rel="noopener noreferrer"><em>dépêche DACG du 17 juillet 2019</em>
<ExternalLinkIcon/></a><em>;</em>
<a href="https://www.legifrance.gouv.fr/affichTexte.do?cidTexte=JORFTEXT000038480349&amp;categorieLien=cid" target="_blank" rel="noopener noreferrer"><em>décret du 16 mai
2019</em><ExternalLinkIcon/></a><em>)</em></p>
<p>Le SEFJ est issu d'une transformation de l'ancien SNDJ (<em>Cf</em> <a href="http://intranet.justice.gouv.fr/site/dacg/art_pix/douane_judiciaire.pdf" target="_blank" rel="noopener noreferrer">fiche DACG - mars
2016<ExternalLinkIcon/></a>).</p>
<p>SEFJ est composé de douaniers habilités en qualité d'officiers de douane
judiciaire (ODJ ; <a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?cidTexte=LEGITEXT000006071154&amp;idArticle=LEGIARTI000006574902&amp;dateTexte&amp;categorieLien=cid" target="_blank" rel="noopener noreferrer">art
28-1<ExternalLinkIcon/></a>)
et d'agents de la DGFiP habilités en qualité d'officiers fiscaux judiciaires
(OFJ ; <a href="https://www.legifrance.gouv.fr/affichCodeArticle.do%3Bjsessionid%3D57D734E2C45C23086CF9C91FF1F04C65.tplgfr38s_2?idArticle=LEGIARTI000037525995&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20190718&amp;categorieLien=id&amp;oldAction&amp;nbResultRech" target="_blank" rel="noopener noreferrer">art
28-2<ExternalLinkIcon/></a>).
ODJ et OFJ ont les mêmes pouvoirs que les OPJ mais ne peuvent s'auto-saisir (il
faut une saisine judiciaire : PR ou JI).</p>
<p>SEFJ est un service à compétence nationale fonctionnant comme un office central
toutefois conçu avec des unités locales et des antennes.</p>
<p>SEFJ est compétent pour les infractions liées au capitaux, fraude fiscale,
contrefaçons, obligations déclaratives, trafic d'armes, jeux, intérêts
financiers de l'UE, asso de malfaiteurs en lien avec ces infractions,
blanchiment... Pour plusieurs infractions comme le trafic de stup, il faut une
cosaisine avec PN / GN (on parlera alors d'unité mixte).</p>
