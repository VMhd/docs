<h2 id="police-nationale-securite-publique" tabindex="-1"><a class="header-anchor" href="#police-nationale-securite-publique" aria-hidden="true">#</a> Police nationale - sécurité publique</h2>
<p>Le territoire est découpé en 6 directions zonales de sécurité publique (DZSP).</p>
<p>Au niveau du département, le directeur départemental de la sécurité publique
(DDSP) dirige la sécurité publique. À l'échelon départemental, on trouve des
services spécifiques comme le centre d'information et de commandement (CIC), qui
reçoit les appels 17 et envoie les effectifs des SSIAP sur place. On trouve un
commissariat par circonscription de sécurité publique (CSP).</p>
<p>L'organisation des commissariats, et plus particulièrement l'appellation des
groupes, varie souvent d'une DDSP à l'autre. L'organisation la plus répandue est
la suivante :</p>
<ul>
<li>
<p>la <strong>sûreté</strong>, qui ne fait que du judiciaire (dans les petits ciat, on parle
sûreté urbaine dite &quot;<em>SU</em>&quot; ou de brigade de sûreté urbaine dite &quot;<em>BSU</em>&quot;).</p>
<p>Au sein de la sûreté, on distingue :</p>
</li>
<li>
<p>le <em>quart</em> aussi appelé <em>GAJ</em> (groupe d'appui judiciaire) ou <em>UTFD</em> (unité
de traitement du flagrant délit) ou <em>UTTR</em> (unité de traitement en temps
réel). Le quart est le &quot;<em>groupe ramassage</em>&quot; auquel le SSIAP ramène les MEC
après interpellation (premier niveau judiciaire). Il mènera les enquêtes
sauf si les groupes d'investigation spécialisés ou la PJ se saisissent de
l'affaire. Le GAJ prend également les plaintes des personnes se présentant à
l'accueil du ciat (on appelle ces fonctionnaires les <em>plaintiers</em>).</p>
</li>
<li>
<p>les groupes d'investigation spécialisés :</p>
</li>
<li>
<p>unité de recherches judiciaires (URJ) : groupe de voie publique, stup,
cellule anti-cambriolage, brigade criminelle,</p>
</li>
<li>
<p>unité de protection sociale (UPS) : brigade de protection des familles,
mineurs, mœurs / débit de boisson, personnes vulnérables,</p>
</li>
<li>
<p>unité de police administrative (UPA) : affaires financières &amp; délégations
judiciaires.</p>
<ul>
<li>le service de sécurité, d'intervention et d'assistance de proximité
(<strong>SSIAP</strong>) anciennement</li>
</ul>
</li>
</ul>
<p>appelé SSP ou police secours (<em>PS</em>). Il comprend les postes de police, les
voitures qui patrouillent, la BAC, l'unité cynophile, les motocyclistes, les
brigades de transfèrement, la BADR (accidents et délits routiers), <em>etc.</em> À
