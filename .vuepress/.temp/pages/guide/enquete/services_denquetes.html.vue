cette zone...), de contrôle transfrontalier et d'éloignement.</p>
<h2 id="inspection-generale-de-la-police-nationale-igpn" tabindex="-1"><a class="header-anchor" href="#inspection-generale-de-la-police-nationale-igpn" aria-hidden="true">#</a> Inspection générale de la police nationale (IGPN)</h2>
<p>IGPN située à Paris mais 9 implantations sur le territoire. Compétente pour tous
les agents relevant de la DGPN, DGSI et PP. Elle peut réaliser une enquête
administrative, judiciaire ou les 2.</p>
<p>Enquête administrative pré-disciplinaire exclusivement initiée par le pouvoir
administratif sur les 22 manquements professionnels et disciplinaires prévus par
les art <a href="https://www.legifrance.gouv.fr/affichCodeArticle.do%3Bjsessionid%3DB074CCE43375E3E815ABD226DCDC4E74.tplgfr36s_3?idArticle=LEGIARTI000028285867&amp;cidTexte=LEGITEXT000025503132&amp;dateTexte=20140101&amp;categorieLien=id&amp;oldAction&amp;nbResultRech" target="_blank" rel="noopener noreferrer">R.
434-4<ExternalLinkIcon/></a>
et s. CSI ; manquements ne recoupant pas des infractions pénales. Le parquet ne
peut imposer une enquête administrative ou solliciter</p>
<p>une sanction disciplinaire mais il peut demander si une sanction a été prononcée
pour classer 61.</p>
<p>L'enquête administrative est transmise au judiciaire pour alimenter la procédure
judiciaire sur réquisition ou, d'initiative, sur le fondement de l'<a href="https://www.legifrance.gouv.fr/affichCodeArticle.do?idArticle=LEGIARTI000006574933&amp;cidTexte=LEGITEXT000006071154&amp;dateTexte=20040310" target="_blank" rel="noopener noreferrer">art
40<ExternalLinkIcon/></a>.</p>
<p>Le pénal ne tient pas le disciplinaire en l'état mais autorité de chose jugée du
pénal sur la seule matérialité des faits.</p>
<h2 id="prefecture-de-police-de-paris-pp" tabindex="-1"><a class="header-anchor" href="#prefecture-de-police-de-paris-pp" aria-hidden="true">#</a> Préfecture de police de Paris (PP)</h2>
<p>La préfecture de police (PP) est compétente pour Paris et, depuis 2009, la
petite couronne. Ses effectifs dépendent du seul préfet de police (ni de la
DCSP, ni de la DCPJ), qui est lui-même sous la responsabilité directe du
ministre de l'intérieur (créé en 2012, le préfet de police des Bouches-du-Rhône
n'a, pour sa part, ni administration propre, ni compétences spécifiques et n'a
qu'une autorité fonctionnelle sur les services relevant de la DGPN et DGGN).</p>
<p>Les services actifs de la PP sont répartis entre les directions suivantes :</p>
<ul>
<li>
<p>direction de la sécurité de proximité de l'agglomération parisienne (DSPAP)
;</p>
</li>
<li>
<p>direction de l'ordre public et de la circulation (DOPC) en charge du
maintien de l'ordre et de la circulation routière ;</p>
